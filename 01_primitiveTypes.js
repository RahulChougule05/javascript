
// i) Primitive Data Type
// 1. Number
var age = 31;
var weight = 72;
var height = 6.3;
var specNumber = 0.50;

var typeOfAge = typeof age;
console.log("Type of Variable age:", typeOfAge);

console.log("Type of Variable specNumber:",typeof specNumber);
console.log("Age:",age, "Weight:", weight, "Height:", height, "SpecNumber:", specNumber);

console.log("Age:", age);
console.log("Weight:", weight);
console.log("Height:", height);
console.log("SpecNumber:",specNumber);

// 2. Boolean --> true or false
var isMarried = false;
var knowAngular = true; 
var isSoftwareEngineer = true;
var isBachelor = true; 

console.log("isBachelor:", isBachelor);
console.log("Type of Variable isMarried:", typeof isMarried);

// 3. String --> Sequence of character enclosed in '' or ""
var firstName = "Bill";
var position = 'Microsoft Co-founder'; 
var city="";
console.log("Type of Variable city:",typeof city);
var typeOfFirstName = typeof firstName;
console.log("Type of Variable firstName:", typeOfFirstName );
console.log("Type of Variable position:" , typeof position);

// ii) Special Data Type
//1. Undefined
var address;
console.log("Address:", address);
console.log("type of Variable address:",typeof address);

//2. null
var pin = null;
console.log("Value of Variable pin:", pin);
console.log("Type of Variable pin:", typeof pin);
